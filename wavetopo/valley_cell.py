def triangle_ribbon(Nx, alphaL_deg, alphaR_deg, a=1.0, R=0.44, h=0.06,
                    verbose=False):
    """Ribbon supercell: Nx triangular-lattice cells along a1 (finite, open),
    one cell along a2 (Bloch-periodic).  Triangles rotated alphaL for the left
    half and alphaR for the right half -> a domain wall at the centre.  Returns
    a mesh dict plus a2-only periodic data for a kappa=k.a2 sweep."""
    import gmsh
    a1 = a * np.array([1.0, 0.0]); a2 = a * np.array([0.5, np.sqrt(3) / 2])
    gmsh.initialize(); gmsh.option.setNumber("General.Terminal", 1 if verbose else 0)
    gmsh.model.add("ribbon"); occ = gmsh.model.occ
    # big parallelogram Nx*a1 x a2
    A1 = Nx * a1
    P = [occ.addPoint(0, 0, 0), occ.addPoint(*A1, 0),
         occ.addPoint(*(A1 + a2), 0), occ.addPoint(*a2, 0)]
    L = [occ.addLine(P[0], P[1]), occ.addLine(P[1], P[2]),
         occ.addLine(P[2], P[3]), occ.addLine(P[3], P[0])]
    cell = occ.addPlaneSurface([occ.addCurveLoop(L)])
    inc = []
    for i in range(Nx):
        al = np.deg2rad(alphaL_deg if i < Nx // 2 else alphaR_deg)
        ctr = i * a1 + (a1 + a2) / 2
        v = [ctr + R * np.array([np.cos(al + np.pi/2 + 2*np.pi*k/3),
                                 np.sin(al + np.pi/2 + 2*np.pi*k/3)]) for k in range(3)]
        pts = [occ.addPoint(*vk, 0) for vk in v]
        ln = [occ.addLine(pts[k], pts[(k+1) % 3]) for k in range(3)]
        inc.append(occ.addPlaneSurface([occ.addCurveLoop(ln)]))
    occ.fragment([(2, cell)], [(2, t) for t in inc]); occ.synchronize()
    areas = {tag: gmsh.model.occ.getMass(2, tag) for (_, tag) in gmsh.model.getEntities(2)}
    matrix_tag = max(areas, key=areas.get)
    fiber = [t for t in areas if t != matrix_tag]
    # a2-periodic edges: bottom (P0->P1) and top (P3->P2)
    bot, top = [], []
    for (_, tag) in gmsh.model.getEntities(1):
        mid = np.array(gmsh.model.occ.getCenterOfMass(1, tag)[:2])
        for name, p0, d in [("bot", np.zeros(2), A1), ("top", a2, A1)]:
            tt = np.dot(mid - p0, d) / np.dot(d, d)
            if 0 <= tt <= 1 and np.hypot(*(mid - (p0 + tt * d))) < 1e-6:
                (bot if name == "bot" else top).append(tag)
    T = np.eye(4); T[0, 3], T[1, 3] = a2[0], a2[1]
    gmsh.model.mesh.setPeriodic(1, top, bot, T.flatten().tolist())
    gmsh.option.setNumber("Mesh.MeshSizeMax", h); gmsh.option.setNumber("Mesh.MeshSizeMin", 0.6*h)
    gmsh.model.mesh.generate(2)
    ntag, nc, _ = gmsh.model.mesh.getNodes(); npx = nc.reshape(-1, 3)[:, :2]
    t2i = {int(t): i for i, t in enumerate(ntag)}
    tris, zf = [], []
    for surf in ([matrix_tag] + fiber):
        en = gmsh.model.mesh.getElementsByType(2, surf)[1]
        for k in range(len(en)//3):
            tris.append([t2i[int(en[3*k+r])] for r in range(3)])
            zf.append(0.0 if surf == matrix_tag else 1.0)
    tris = np.array(tris, np.int64); zfib = np.array(zf)
    sa2 = {}
    for tag in top:
        _, sl, ma, _ = gmsh.model.mesh.getPeriodicNodes(1, tag)
        sa2.update({int(s): int(m) for s, m in zip(sl, ma)})
    gmsh.finalize()
    Nn = npx.shape[0]; i2t = {i: int(t) for t, i in t2i.items()}
    master_t = np.arange(Nn); sph = np.zeros(Nn)
    for i in range(Nn):
        tag = i2t[i]
        if tag in sa2:
            master_t[i] = t2i[sa2[tag]]; sph[i] = 1.0
    uniq = np.unique(master_t); remap = {int(m): r for r, m in enumerate(uniq)}
    master = np.array([remap[int(m)] for m in master_t])
    return dict(npx=npx, tris=tris, zfib=zfib, master=master, sphase=sph,
                n_red=len(uniq), a1=a1, a2=a2, Nx=Nx, R=R)
